package exercitiul1;

public class Persoana {
	// proprietati
	private String nume;
	private int varsta;
	
	// constructor
	public Persoana(String nume, int varsta)
	{
		this.nume = nume;
		this.varsta = varsta;
	}

	// getteri
	public String getNume() {
		return nume;
	}

	public int getVarsta() {
		return varsta;
	}

	
}
